const translations = {
    "title": { "en": "Trivergents", "ja": "トライヴァージェンツ" },
    "description": { "en": englishBlurb, "ja": japaneseBlurb },
    "profile": { "en": englishProfile, "ja": japaneseProfile },
    "michal": { "en": englishMichal, "ja": japaneseMichal },
    "danny": { "en": englishDanny, "ja": japaneseDanny },
    "dean": { "en": englishDean, "ja": japaneseDean },
};

window.onload = function () {
    const menu_btn = document.querySelector('.hamburger');
    const mobile_menu = document.querySelector('.mobile-nav');
    const promotion_button = document.querySelector('.buy-album');
    const menu_items = document.querySelectorAll('.mobile-nav a');
    const langSwitch = document.getElementById('langSwitch');
    let currentLang = 'en';

    function updateFlags() {
        const enFlags = document.querySelectorAll('#enFlagMain, #enFlagMobile');
        const jaFlags = document.querySelectorAll('#jaFlagMain, #jaFlagMobile');
        
        enFlags.forEach(flag => flag.style.display = currentLang === 'ja' ? 'inline' : 'none');
        jaFlags.forEach(flag => flag.style.display = currentLang === 'en' ? 'inline' : 'none');
    }

    function setInitialContent() {
        document.querySelector('.hero .content p').textContent = translations.description[currentLang];
        document.querySelector('.profile').innerHTML = translations.profile[currentLang];
        document.querySelector('.michal').textContent = translations.michal[currentLang];
        document.querySelector('.danny').textContent = translations.danny[currentLang];
        document.querySelector('.dean').textContent = translations.dean[currentLang];
    }

    updateFlags();
    setInitialContent();

    function toggleMenu() {
        menu_btn.classList.toggle('is-active');
        mobile_menu.classList.toggle('is-active');
        promotion_button.classList.toggle('is-active');
    }

    menu_btn.addEventListener('click', toggleMenu);

    menu_items.forEach(item => {
        item.addEventListener('click', function() {
            if (mobile_menu.classList.contains('is-active')) {
                toggleMenu();
            }
        });
    });

    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener("click", function(e) {
            e.preventDefault();
            document.querySelector(this.getAttribute("href")).scrollIntoView({
                behavior: "smooth"
            });
        });
    });

    function switchLanguage() {
        currentLang = currentLang === 'en' ? 'ja' : 'en';
        updateFlags();
        setInitialContent();
    }

    const langSwitchButtons = document.querySelectorAll('#langSwitchMain, #langSwitchMobile');
    langSwitchButtons.forEach(button => {
    button.addEventListener('click', switchLanguage);
});
};